package ActionItem;

public class ActionItem_01 {
    public static void main(String[] args) {

        //declare a string variable as state
        String stateVariable = "New York";

        //declare an integer variable as area code
        int areaVariable = 347;

        //declare a string variable as zipcode
        String zipVariable = "11102";

        //print out the variables and include spaces
        System.out.println(stateVariable + " " + areaVariable + " " + zipVariable);

    }//end of main method

}//end of action item
